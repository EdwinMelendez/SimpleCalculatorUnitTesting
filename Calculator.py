import math

class calc:

    def add(self, a, b):
        print('Answer: ' + str(a + b))

    def sub(self, a, b):
        print('Answer: ' + str(a - b))

    def mult(self, a, b):
        print('Answer: ' + str(a * b))

    def div(self, a, b):
        print('Answer: ' + str(a / b))

    def square(self, a):
        print('Answer: ' + str(math.sqrt(a)))
